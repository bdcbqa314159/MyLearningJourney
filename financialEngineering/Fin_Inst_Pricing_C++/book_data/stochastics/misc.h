/*
 * Macros
 *
 *      NULL    Null pointer value.
 *      TRUE    Value for TRUE, cast as an int.
 *      FALSE   Value for FALSE, cast as an int.
 */

#ifndef NULL
#define NULL    ((void *) 0)
#endif

#ifndef TRUE
#define TRUE    ((int) 1)
#endif

#ifndef FALSE
#define FALSE   ((int) 0)
#endif



